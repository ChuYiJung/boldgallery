<h1>Music instruments</h1>
  <p>
    <img src="http://www.wellspianos.com/sites/default/files/sites/default/files/images/commerce/DSC_0513.JPG" id="piano"></img>
    The story of the piano begins in Padua, Italy in 1709, in the shop of a harpsichord maker named Bartolomeo di Francesco Cristofori (1655-1731). Many other stringed and keyboard instruments preceded the piano and led to the development of the instrument as we know it today.Eventually, a family of stringed instruments with a keyboard evolved in Europe in the 14th century. The earliest of these was a dulcimer, a closed, shallow box over which stretched wires were struck with two wooden hammers. The dulcimer led to the development of the clavichord, which also appeared in the 14th century. These were followed by the spinet, virginal, clavecin, gravicembalo, and finally, the harpsichord in the 15th century.The harpsichord, however, was limited to one, unvarying volume. Its softness and loudness could not be varied while playing. Therefore, performing artists could not convey the same degree of musical expression as that of most other instruments. The artistic desire for more controlled expression led directly to the invention of the piano, on which the artist could alter the loudness and tone with the force of one’s fingers.</p>
